# string frunctions

# Create a variable full_name and give a value
full_name = "Gideon Kiplangat"

#print out the variable
print(full_name)

#Change the content of the variable into uppercase
print(full_name.upper())

#Change the content of the variable into lowercase
print(full_name.lower())

# Change the content of the variable to be capitalize
print(full_name.capitalize())

# replace the existence of G with K
print(full_name.replace("G", "K"))

# check for uppercase
print(full_name.isupper())

#check for lowercase
print(full_name.islower())

#check for digit
print(full_name.isdigit())

# Change to upper and check for upper
print(full_name.upper().isupper())

#change to lower and check for lower
print(full_name.lower().islower())

# find the index position of l
print(full_name.find("l"))

#check for the number of occurence of i
print(full_name.count("i"))



